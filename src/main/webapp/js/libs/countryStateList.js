function loadStates(countryName) {
    if(countryName == "US"){
        $("#USA_State").show().val(0);
        $("#USA_State").prop("required", true);
        $("#BillingState").val("");
        $("#BillingState").hide();
        $("#BillingState").prop("required", false);
    }else{
        $("#BillingState").val("");    
        $("#BillingState").hide();
        $("#BillingState").prop("required", false);
        $("#USA_State").val(0);
        $("#USA_State").hide();
        $("#USA_State").prop("required", false);
    }
}

